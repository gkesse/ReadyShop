$(document).ready(function () {
    //===============================================
    // header
    //===============================================
    // header icon
    $(".hdmb").click(function () {
        $(".hdmn").toggleClass("rwd");
        
        if($(".hdmb").html() == '<i class="fa fa-bars"></i>'){
            $(".hdmb").html('<i class="fa fa-close"></i>');
        }
        else {
            $(".hdmb").html('<i class="fa fa-bars"></i>');
        }
    });
    //===============================================
})();
